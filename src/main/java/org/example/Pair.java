package org.example;

public class Pair {
    private String ICase;
    private String uCase;

    public Pair(String ICase, String uCase) {
        this.ICase = ICase;
        this.uCase = uCase;
    }

    public String getICase() {
        return ICase;
    }

    public String getuCase() {
        return uCase;
    }


}

